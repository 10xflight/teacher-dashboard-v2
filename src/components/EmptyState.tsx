'use client';

import React from 'react';
import Link from 'next/link';

// ============================================================
// Reusable EmptyState component
// ============================================================

interface EmptyStateProps {
  icon?: React.ReactNode;
  preset?: keyof typeof PRESET_ICONS;
  title: string;
  description?: string;
  action?: {
    label: string;
    href?: string;
    onClick?: () => void;
  };
  compact?: boolean; // For inline / section-level empty states
}

export default function EmptyState({
  icon,
  preset,
  title,
  description,
  action,
  compact = false,
}: EmptyStateProps) {
  const iconNode = icon || (preset ? PRESET_ICONS[preset] : null);

  if (compact) {
    return (
      <div className="flex flex-col items-center gap-1.5 py-6 px-4">
        {iconNode && (
          <span className="text-text-muted/50">{iconNode}</span>
        )}
        <p className="text-sm text-text-muted text-center">{title}</p>
        {description && (
          <p className="text-xs text-text-muted/70 text-center max-w-xs">{description}</p>
        )}
        {action && <EmptyStateAction action={action} small />}
      </div>
    );
  }

  return (
    <div className="flex flex-col items-center justify-center py-16 px-6">
      {iconNode && (
        <div className="w-14 h-14 rounded-2xl bg-accent/8 border border-accent/15 flex items-center justify-center mb-4">
          <span className="text-accent/60">{iconNode}</span>
        </div>
      )}
      <h3 className="text-base font-semibold text-text-primary mb-1">{title}</h3>
      {description && (
        <p className="text-sm text-text-muted text-center max-w-sm leading-relaxed">{description}</p>
      )}
      {action && <EmptyStateAction action={action} />}
    </div>
  );
}

// ============================================================
// Action Button / Link
// ============================================================

function EmptyStateAction({
  action,
  small,
}: {
  action: NonNullable<EmptyStateProps['action']>;
  small?: boolean;
}) {
  const cls = small
    ? 'mt-2 px-3 py-1.5 text-xs font-medium text-accent border border-accent/30 rounded-lg hover:bg-accent/10 transition-colors'
    : 'mt-4 px-4 py-2 text-sm font-semibold text-accent border border-accent/30 rounded-lg hover:bg-accent/10 transition-colors';

  if (action.href) {
    return (
      <Link href={action.href} className={cls}>
        {action.label}
      </Link>
    );
  }

  return (
    <button onClick={action.onClick} className={cls}>
      {action.label}
    </button>
  );
}

